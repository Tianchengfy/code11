# 获取源文件夹中所有的.txt文件
all_files <- list.files(path = source_dir, full.names = TRUE)

# 循环遍历所有文件，检查它们是否以指定的前缀开始
for (file_path in all_files) {
  # 获取文件的基本名称（不包含路径）
  file_name <- basename(file_path)
  
  # 检查文件名是否以任一指定前缀开始
  if (any(sapply(file_prefixes, function(prefix) grepl(pattern = paste0("^", prefix), x = file_name)))) {
    # 如果是，移动文件到目标文件夹
    file.rename(file_path, file.path(target_dir, file_name))
  }
}
